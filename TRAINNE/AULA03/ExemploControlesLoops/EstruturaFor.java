package ExemploControlesLoops;

public class EstruturaFor {
    public static void main(String[] args) {
        // A variável cont pode ser criada aqui
        // int cont;

        // Ou variável cont pode ser criada dentro do for
        for (int cont = 1; cont <= 10; cont++) { // cont++  <==> cont=cont+1
            System.out.println("Numero ==> " + cont);
        }
    }
}


