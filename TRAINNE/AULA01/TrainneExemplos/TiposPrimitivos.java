public class TiposPrimitivos{
    public static void main(String args[]){
        char sexo = 'f';
        byte idade = 16;
        short codigo = 256;
        float nota = 9.4f;
        int alunos = 100, classes = 10;
        long habitantes = 8202023;
        double dolar = 5.77;
        boolean alternativa = false;

        System.out.println("\nSexo: " + sexo + " idade: " + idade + " Codigo: " + codigo);
        System.out.println("Nota: " + nota + " alunos: " + alunos + " classe: " + classes);
        System.out.println("Habiantes: " + habitantes + " dolar: " + dolar + " alternativa: " + alternativa);
    }
}
